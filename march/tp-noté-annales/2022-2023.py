def capaTotale(stations: list[Station]) -> int:
    c = 0
    for s in stations:
        c += s.numberBorn
    return c


def capaMax(stations: list[Station]) -> list[str | int]:
    st = []
    m = 0
    for s in stations:
        if s.numberBorn > m:
            st = [s]
            m = s.numberBorn
    return [m] + [i.name for i in st]


def dico(stations: list[Station]) -> dict[str, tuple[int]]:
    dico = {}
    for s in stations:
        if s.zipCode in dico:
            d = dico[s.zipCode]
            l = (d[0] + 1, d[1] + s.numberBorn)
            dico[s.zipCode] = l
        else:
            l = (1, s.numberBorn)
            dico[s.zipCode] = l
    return dico


def dis(st: Station, s: Station) -> float:
    r = 6371
    one = ((st.lattitude - s.lattitude) * math.pi * r) / 180
    two = ((st.longitude - s.longitude) * math.pi * r) / 180
    return math.sqrt(one ** 2 + two ** 2)


def disMax(stations: list[Station]) -> dict[str, int | tuple]:
    v = {}
    for st in stations:
        for s in stations:
            if s == st:
                continue
            l = dis(st, s)
            if v == {} or v["l"] <= l:
                v["l"] = l
                v["name"] = (st.name, s.name)
                v["city"] = (st.city, s.city)
    return v


def lire(f: str) -> list[list[str]]:
    return [l.split(",") for l in open(f, "r").readlines()]


