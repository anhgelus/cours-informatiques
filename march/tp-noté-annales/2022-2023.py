class Station:
    def __init__(self, name: str, longitude: float, lattitude: float, numberBorn: int, number: int, city: str,
                 region: str, regionCode: str, zipCode: int):
        self.name = name
        self.longitude = longitude
        self.lattitude = lattitude
        self.numberBorn = numberBorn
        self.number = number
        self.city = city
        self.region = region
        self.regionCode = regionCode
        self.zipCode = zipCode


